package JavaTopics;

public class IfAndElse {
    public static void main(String[] args) {

        int a = 6, b = 9;
        System.out.println(b < a);

        if (a < b) {
            System.out.println("a'nın değeri b'den küçüktür");
        }else {
            System.out.println("b'nin değeri a'dan küçüktür");
        }

    }
}
