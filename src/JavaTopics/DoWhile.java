package JavaTopics;

public class DoWhile {
    public static void main(String[] args) {

        int a = 5;

        while (a < 10) {
            System.out.println("Hello");
            a++;
        }

        System.out.println("--------------------");

        int b = 10;

        do {
            System.out.println("Merhaba");
            b++;
        } while (b < 10);

    }
}
