package JavaTopics;

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("ABC 123");
        System.out.println("This \"fish\" is blue");

        //Backslash çizgisi ile istenilen ifade tırnak içerisine alınabilir. Backslash AltGr + ? tuşu ile yapılır.

        System.out.println("These are double quotes \"\"");
        System.out.println("This is a backslash \\");
        System.out.println("What do you mean \"stinky\"");
        System.out.println("The file is located at C:\\Desktop\\FunnyCatPics");
        System.out.println("This is a tab \t!");
        System.out.println("This is a \n new \n line");

    }
}
