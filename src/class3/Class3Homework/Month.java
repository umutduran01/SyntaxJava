package class3.Class3Homework;

public class Month {
    public static void main(String[] args) {

        int month = 4;

        if (month == 1) {
            System.out.println("January");
        }else if (month == 2) {
            System.out.println("February");
        }else if (month == 3) {
            System.out.println("March");
        }else if (month == 4) {
            System.out.println("April");
        }else if (month == 5) {
            System.out.println("May");
        }

    }
}
