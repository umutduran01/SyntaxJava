package class3;

public class ModulusOperator {
    public static void main(String[] args) {

        int number = 10;
        System.out.println(number/3);

        //int kullanıldığı için ondalıklı sonuç çıkmaz.

        System.out.println(number%3);

        //modulus (%) ile bölümden kalanı buluruz.

    }
}
