package class2;

public class OperatorsDemo {
    public static void main(String[] args) {

        int number = 10;
        System.out.println(number);

        int number1 = 10;
        int number2 = 20;
        System.out.println(number1 + number2);
        System.out.println(number1 - number2);
        System.out.println(number1 * number2);
        System.out.println(number1 / number2);

    }
}
