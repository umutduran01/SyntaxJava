package class6;

public class WhileLoop {
    public static void main(String[] args) {

        int number = 0;
        while (number < 3){
            System.out.println("Hello World");
            number++;
        }

        //Yazdığımız kondisyon doğru olduğu sürece bu kod çalışacak. Yanlış olduğu yerde duracak.

    }
}
