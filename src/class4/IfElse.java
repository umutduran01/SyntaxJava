package class4;

public class IfElse {
    public static void main(String[] args) {


        //EXAMPLE 1

        boolean rain = true;

        if (rain) {
            System.out.println("Take an umbrealla.");
        }else {
            System.out.println("Just take a hat.");
        }


        //EXAMPLE 2

        boolean breakTime = true;

        if (breakTime) {
            System.out.println("Let's take a break");
        }else {
            System.out.println("Keep the class");
        }
    }
}
